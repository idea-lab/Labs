#Creating a list
myList = [1, 2, 3, 4, 5, 6]
print(myList) # [1, 2, 3, 4, 5, 6]
#Accessing specific values (ZERO INDEXING)
print(myList[0]) # 1
#Changing specific values
myList[3]=10
print(myList) # [1, 2, 3, 10, 5, 6]
#Array Slicing (python specific) -- gets a subset of array
print(myList[2:4]) # [3, 10]
print(myList[:4]) # [1, 2, 3, 10] -- up to index 4 (exclusive)
